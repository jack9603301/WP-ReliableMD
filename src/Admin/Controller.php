<?php

namespace WPReliableMD\Admin;

class Controller {
	public function __construct() {

		//Javascript 文件
		add_action('admin_enqueue_scripts',array($this,'enqueue_scripts'));
		//CSS 文件
		add_action('admin_enqueue_scripts',array($this,'enqueue_style'));
		add_filter( 'admin_body_class', array($this,'WPReliableMD_admin_body_class'));
	}

	public function enqueue_scripts() {
		//Requirejs库
		wp_enqueue_script('jquery');
		wp_enqueue_script('markdown-it', WPReliableMD_URL.'/bower_components/markdown-it/dist/markdown-it.js', array(), WPReliableMD_VER, true);
		wp_enqueue_script('to-mark', WPReliableMD_URL.'/bower_components/to-mark/dist/to-mark.js', array(), WPReliableMD_VER, true);
		wp_enqueue_script('tui-code-snippet', WPReliableMD_URL.'/bower_components/tui-code-snippet/dist/tui-code-snippet.js', array(), WPReliableMD_VER, true);
		wp_enqueue_script('tui-color-picker', WPReliableMD_URL.'/bower_components/tui-color-picker/dist/tui-color-picker.js', array(), WPReliableMD_VER, true);
		wp_enqueue_script('codemirror', WPReliableMD_URL.'/bower_components/codemirror/lib/codemirror.js', array(), WPReliableMD_VER, true);
		wp_enqueue_script('highlightjs', WPReliableMD_URL.'/bower_components/highlightjs/highlight.pack.js', array(), WPReliableMD_VER, true);
		wp_enqueue_script('squire-rte', WPReliableMD_URL.'/bower_components/squire-rte/build/squire-raw.js', array(), WPReliableMD_VER, true);
		wp_enqueue_script('plantuml-encoder', WPReliableMD_URL.'/bower_components/plantuml-encoder/dist/plantuml-encoder.js', array(), WPReliableMD_VER, true);
		wp_enqueue_script('raphael', WPReliableMD_URL.'/bower_components/raphael/raphael.js', array(), WPReliableMD_VER, true);
		wp_enqueue_script('tui-chart', WPReliableMD_URL.'/bower_components/tui-chart/dist/tui-chart.js', array(), WPReliableMD_VER, true);
		wp_enqueue_script('tui-editor', WPReliableMD_URL.'/bower_components/tui-editor/dist/tui-editor-Editor-all.js', array(), WPReliableMD_VER, true);
		wp_enqueue_script( 'ReliableMD', WPReliableMD_URL . '/js/WPReliableMD_Admin.js', array(), WPReliableMD_VER, true );
	}

	public function enqueue_style() {
		//Requirejs库
		wp_enqueue_style('normalize', '//necolas.github.io/normalize.css/8.0.0/normalize.css', array(), WPReliableMD_VER, true);
		wp_enqueue_style('codemirror', WPReliableMD_URL.'/bower_components/codemirror/lib/codemirror.css', array(), WPReliableMD_VER, true);
		wp_enqueue_style('github', WPReliableMD_URL.'/bower_components/highlightjs/styles/github.css', array(), WPReliableMD_VER, true);
		wp_enqueue_style('tui-editor', WPReliableMD_URL.'/bower_components/tui-editor/dist/tui-editor.css', array(), WPReliableMD_VER, true);
		wp_enqueue_style('tui-editor', WPReliableMD_URL.'/bower_components/tui-editor/dist/tui-editor-contents.css', array(), WPReliableMD_VER, true);
		wp_enqueue_style('tui-color-picker', WPReliableMD_URL.'/bower_components/tui-color-picker/dist/tui-color-picker.css', array(), WPReliableMD_VER, true);
		wp_enqueue_style('tui-chart', WPReliableMD_URL.'/bower_components/tui-chart/dist/tui-chart.css', array(), WPReliableMD_VER, true);
		wp_enqueue_style( 'ReliableMD', WPReliableMD_URL . '/css/WPReliableMD_Admin.css', array(), WPReliableMD_VER, true );
	}

	public function WPReliableMD_admin_body_class() {
		if ( current_theme_supports( 'editor-styles' ) && current_theme_supports( 'dark-editor-style' ) ) {
		 	return "$classes reliablemd-editor-page is-fullscreen-mode is-dark-theme";
		} else {
			// Default to is-fullscreen-mode to avoid jumps in the UI.
			return "$classes reliablemd-editor-page is-fullscreen-mode";
		}
	}
}

?>